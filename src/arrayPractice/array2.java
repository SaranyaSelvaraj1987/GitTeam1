package arrayPractice;

public class array2 {

	public static void main(String[] args) {
		//Reverse array
		int b[] = new int[10];
		b[0]=1;
		b[1]=2;
		b[2]=3;
		b[3]=4;
		b[4]=5;
		b[5]=6;
		b[6]=7;
		b[7]=8;
		b[8]=9;
		b[9]=10;
		for(int i=9;i>-1;i--)
		{
		System.out.println("Value of"+i+":"+b[i]);	
		}
		
	}

}
